package 자료형;

public class _01_DataTypes{
    public static void main(String[] args) {        //main or psvm
        System.out.println("Hello World");      //sout  문자열
        System.out.println(20230926);       //숫자자료형
        System.out.println(-20230926);
        System.out.println(3.14);
        System.out.println(true);       //boolean자료형
        System.out.println(false);

        System.out.println(123 + 345 );
    }
}
