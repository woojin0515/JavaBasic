package 기본편;

public class _09_Operator3 {
    public static void main(String[] args) {
        // 비교 연산자
        System.out.println(5 > 3);  // 5 는 3 보다 크다
        System.out.println(5 >= 3); // 5 는 3 보다 크거나 같다
        System.out.println(5 >= 5); // true
        System.out.println(5 >= 7);  // false

        System.out.println(5 < 3);  // 5 는 3 보다 작다
        System.out.println(5 <= 3); // 5 는 3 보다 작거나 같다

        System.out.println(5 == 5); // 5 는 5 와 같다
        System.out.println(5 != 5); // 5 는 5 와 다르다
        // boolean 형으로 나온다
    }
}
